package com.osrs.bean;

public class Route {
	private String RouteId;
	private String RouteName;
	
	
	
	
	public String getRouteId() {
	return RouteId;
	}
	public void setRouteId(String routeId) {
		RouteId = routeId;
	}
	public String getRouteName() {
		return RouteName;
	}
	public void setRouteName(String routeName) {
		RouteName = routeName;
	}

}
